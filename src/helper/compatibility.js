if (window.NodeList && !NodeList.prototype.forEach) {
    NodeList.prototype.forEach = function (callback, thisArg) {
        thisArg = thisArg || window;
        for (var i = 0; i < this.length; i++) {
            callback.call(thisArg, this[i], i, this);
        }
    };
}

if (!SVGLineElement.prototype.getTotalLength) {
    SVGLineElement.prototype.getTotalLength = function () {
        return Math.sqrt(Math.pow((this.x1.baseVal.value - this.x2.baseVal.value), 2) +
            Math.pow((this.y1.baseVal.value - this.y2.baseVal.value), 2))
    }
}

if (!Array.prototype.find) {
    // eslint-disable-next-line
    Array.prototype.find = function (predicate) {
        if (this === null) {
            throw new TypeError('Array.prototype.find called on null or undefined');
        }
        if (typeof predicate !== 'function') {
            throw new TypeError('predicate must be a function');
        }
        var list = Object(this);
        var length = list.length >>> 0;
        var thisArg = arguments[1];
        var value;

        for (var i = 0; i < length; i++) {
            value = list[i];
            if (predicate.call(thisArg, value, i, list)) {
                return value;
            }
        }
        return undefined;
    };
}

